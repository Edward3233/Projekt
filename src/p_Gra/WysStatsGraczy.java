    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        
        String imion[] = jTextArea1.getText().split("\n");
        String numery[] = jTextArea2.getText().split("\n");

        
        jTextArea1.setText("");
        jTextArea2.setText("");

        
        for(int i=0; i<imion.length; i++)
            for(int j=0; j<imion.length-1; j++)
                if(imion[j].compareTo(imion[j+1])>0)
                {
                    String a=imion[j];
                    imion[j] = imion[j+1];
                    imion[j+1]=a;
                    String b=numery[j];
                    numery[j] = numery[j+1];
                    numery[j+1]=b;
                }
        
        for(int i=0; i<imion.length; i++)
        {
    
            jTextArea1.append(imion[i]+"\n");
            jTextArea2.append(numery[i]+"\n");
            
        }               
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        String imion[] = jTextArea1.getText().split("\n");
        String numery[] = jTextArea2.getText().split("\n");
        
        int numeryint[] = new int[numery.length];
        
        for(int i = 0; i<numery.length; i++)
            numeryint[i] = Integer.parseInt(numery[i]);
        
        
        jTextArea1.setText("");
        jTextArea2.setText("");

        
        for(int i=0; i<imion.length; i++)
            for(int j=0; j<imion.length-1; j++)
                if(numeryint[j]<(numeryint[j+1]))
                {
                    String a=imion[j];
                    imion[j] = imion[j+1];
                    imion[j+1]=a;

                    int d = numeryint[j];
                    numeryint[j] =numeryint[j+1];
                    numeryint[j+1] =d;
                }
        
        for(int i=0; i<imion.length; i++)
        {
    
            jTextArea1.append(imion[i]+"\n");
            jTextArea2.append(String.valueOf(numeryint[i]) + "\n");
            
        }                   
    }//GEN-LAST:event_jButton2MouseClicked

