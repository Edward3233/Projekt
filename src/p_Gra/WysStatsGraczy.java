    protected WysStatsGraczy() throws FileNotFoundException, IOException {
        initComponents();
        File file = new File("src/statystyki/gracz.txt");
        
        
        FileReader odczyt = new FileReader(file);
        BufferedReader buff = new BufferedReader(odczyt);
        buff.readLine();
        String pom;

        if((pom=buff.readLine())!= null)
        {
        do
        {
          
          
          this.jTextArea1.append(pom + "\n");
          pom = buff.readLine();
          this.jTextArea2.append(pom +"\n");
          
          pom = buff.readLine();
          
        }while(pom !=null);
        }
        else
        {
            this.jTextArea1.append("Brak statystyk");
            this.jTextArea2.append("Brak statystyk");
        }
        
    }

