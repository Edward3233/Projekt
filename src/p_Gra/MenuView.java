    public String getImie()
    {

        
        this.UwagaLabel.setVisible(false);
        this.UwagaLabel1.setVisible(false);
        
        if("".equals(this.jTextField1.getText()) || "".equals(this.jTextField2.getText()))
                {   

                    if("".equals(this.jTextField1.getText()))
                        this.UwagaLabel.setVisible(true);
                    if("".equals(this.jTextField2.getText()))
                        this.UwagaLabel1.setVisible(true);
                    return null;
                }
        else
        {
        this.PytanieImie.setVisible(false);
        //stworzenie form gra z przekazaniem parametru string imie
        
            
        return this.jTextField1.getText();
            
        }
    }
    
